window.showNewsDetail = function(newsId) {
    const modal = document.getElementById('newsModal');
    const modalContent = document.getElementById('newsModalContent');
    const news = newsDetails[newsId];
    
    if (news) {
        modalContent.innerHTML = `
            <div class="news-detail">
                <div class="news-detail-image" style="background-image: url('${news.image}'); height: 400px; background-size: cover; background-position: center; border-radius: 15px; margin-bottom: 20px;"></div>
                <div class="news-detail-meta" style="display: flex; gap: 20px; margin-bottom: 20px;">
                    <span style="color: #d4af37;">${news.date}</span>
                    <span style="background: rgba(212,175,55,0.1); padding: 5px 15px; border-radius: 20px;">${news.category}</span>
                </div>
                <h2 style="color: #d4af37; font-size: 2rem; margin-bottom: 20px;">${news.title}</h2>
                <div class="news-detail-content" style="line-height: 1.8; color: #cccccc;">
                    ${news.content}
                </div>
            </div>
        `;
        modal.style.display = 'flex';
    }
};

